						Al estruendo de los cohetes y retumbar de la música de viento de Las Mojigangas se anuncia el festejo de un enlace matrimonial en la Costa Chica de Guerrero. San Marcos es el municipio de la Costa Chica que durante más de un siglo lleva a cabo esta celebración de alegría, que mezcla coloridos trajes, mezcal de jamaica, y el son de la sanmarqueña familiares recorren las principales calles de la cabecera municipal. Las Mojigangas es una fiesta en la que participan las familias de los novios, cuentan los abuelos que hace años era un festejo de especial y un regalo a la novia por haber llegado virgen al altar.

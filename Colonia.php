					<p>
						Una vez que Tenochtitlán fue destruida y los mexicas sometidos, el Océano Pacífico atrajo la atención de los conquistadores, esencialmente por motivos económicos. En 1519 Hernán Cortés ordenó que se explorara la provincia de Zihuatlán o Zacatula, como la llamaron los conquistadores, para cerciorarse de la existencia del Mar del Sur y de las riquezas en oro y perlas; la expedición estuvo a cargo de Gonzalo de Umbría. A fines de 1520 el mismo Cortés mandó reconocer la región taxqueña, con el objetivo de localizar metales para fundir piezas de artillería. De esta manera, la penetración armada fue formalizando la conquista del territorio sureño, que no opuso gran resistencia, debido al temor que había despertado la noticia de la caída de Tenochtitlán.
						<br> 
						Al conformarse la primera división política de la Nueva España, en el segundo tercio del siglo XVI, se estableció la Real Audiencia y se dividió el territorio de la Nueva España en cinco provincias, dentro de las cuales se establecieron Corregimientos y Alcaldías Mayores que tenían por objeto vigilar el orden de los pueblos y regular las relaciones entre los españoles y las comunidades indígenas. De esta manera se establecieron en el territorio sureño las alcaldías mayores de Tlapa bajo la jurisdicción de la provincia de Puebla; Taxco, Iguala, Chilapa y Acapulco de la de México y Zacatula bajo provincia de Valladolid.

						<br/>  
						
					    <br>  
					   Realizada la conquista, durante la época colonial se hicieron distintas divisiones del territorio de Nueva España. En el siglo XVI fue dividido en dos grandes jurisdicciones político-administrativas: la audiencia de México creada en 1527 y la audiencia de Nueva Galicia en 1548. En un principio Guerrero quedó comprendido dentro de la audiencia de México, pero al crearse la de Nueva Galicia, la provincia de Zacatula quedó de ésta última audiencia y el resto en la de México. Estas dos grandes audiencias quedaron subdivididas, a su vez, en alcaldías, corregimientos y gobiernos; Villaseñor y Sánchez (1746-1748), utilizó esta división para delimitar los obispados de Nueva España en su Theatro Americano. La jurisdicción del estado de Guerrero quedó comprendida dentro de los obispados de Michoacán, México y Puebla.

					    <br/>  
					    <br> 
					    La división en alcaldías y corregimientos permaneció desde 1532 hasta 1786, en esta fecha la Corona española realizó grandes reformas económicas y territoriales en todos sus dominios en Indias. Nueva España, 1786 quedó dividida en 12 intendencias, y el actual estado de Guerrero quedó incluido en su mayor parte en la intendencia de México, y una parte, al noroeste, en la intendencia de Michoacán. Según la Ordenanza de Intendentes ésta región quedó constituida por ocho alcaldías y un gobierno; la alcaldía de Guimeo y Zirándaro, que pertenecía a la intendencia de Michoacán y las alcaldías de: Zacatula, tetela del Río, Zaqualpan y Escateopan; Taxco e Iguala, Tixtla y Chilapa, Tlapa, Igualapa y el gobierno de la Ciudad de los Reyes y puerto de Acapulco que pertenecía a la intendencia de México.  La división en intendencias abolió las alcaldías y corregimientos, y las intendencias quedaron divididas en jurisdicciones menores llamadas subdelegaciones. La zona de Guerrero quedó integrada por ocho subdelegaciones y un gobierno: la subdelegación de Huetamo que pertenecía a la intendencia de Michoacán y las subdelegaciones de Zacatula, Tetela del Río, Zaqualpan, Taxco, Tixtla y Chilapa y el gobierno de Acapulco que pertenecía a la intendencia de México, y la subdelegación de Tlapa que había pasado a pertenecer a la intendencia de Puebla.
					    De estas subdelegaciones se destacaron por su importancia minera la de Zaqualpan por sus minas de plata, y la importancia del mineral de Taxco convirtió a esta población en la más importante de la región sur, por lo que el 22 de mayo de 1783 se estableció en este lugar una diputación de minería que sirvió para gobernar los aspectos mineros dentro de su jurisdicción

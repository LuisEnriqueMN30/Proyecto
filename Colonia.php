					    El puerto de Acapulco era el único habilitado,en el Océano Pacífico, para comerciar con Filipinas, A este puerto llegaba el galeón de Manila con estimadísimas especies, sedas, finísima loza, figuras de marfil, joyas del más variado gusto, toda novedad del Oriente la Nao las transportaba al puerto de Acapulco y a otros de Sudamérica. Aquí se realizaba una feria a la que asistían los comerciantes que iban a vender sus mercancías y adquirir las que llegaban para llevarlas tierra adentro; desde entonces era ya negocio lucrativo para este puerto el alquiler de casas, zaguanes, patios y corrales, y asistencia a los visitantes, el tiempo que duraba la feria. En tiempos de la Colonia ésta era, a grandes rasgos, la situación de la zona que hoy ocupa Guerrero.


